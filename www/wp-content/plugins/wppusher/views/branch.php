<p>
    <strong>Here are a few key differences between Branch and WP Pusher:</strong>
</p>

<ul>
    <li> - Branch can run your build scripts and tests before deployment, whereas WP Pusher is only a deployment tool.</li>
    <li> - WP Pusher is a WordPress plugin, whereas Branch is a hosted service. That means that you can manage all your sites from within one dashboard instead of going to each site.</li>
    <li> - Branch has recipes for many different tasks, such as deployments with Git or RSync, and integrations with different hosting providers, such as Pantheon and WP Engine.</li>
</ul>

