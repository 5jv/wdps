<h2>Announcing Branch - CI/CD for WordPress</h2>

<p>
    <strong>In late 2018, I started working on a new product. It was born out of my experience building and running WP Pusher.</strong>
</p>
<p>
    That product is <a href="https://www.branchci.com/">Branch</a> - A continuous integration and deployment tool for WordPress. And what's that, you might be asking? It's essentially a tool that can automate the building (npm, composer etc.), testing (coding standards, unit tests etc.) and deployment of your WordPress projects.
